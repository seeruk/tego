package tego

import (
	"errors"
	"fmt"
	"strconv"
	"strings"

	"google.golang.org/protobuf/compiler/protogen"
)

const (
	generatedHeader       = "// Code generated by protoc-gen-tego. DO NOT EDIT."
	maxGeneratedLineWidth = 100
	omittableImportPath   = "github.com/seeruk/go-containers/omittable"
	tegoImportPath        = "github.com/seeruk/tego"
)

// GenerateOption configures a Generate call without changing the planned model.
type GenerateOption func(*generateOptions)

type generateOptions struct {
	rpc RPCOptions
}

// WithRPCGeneration controls which planned RPC surfaces are rendered.
func WithRPCGeneration(options RPCOptions) GenerateOption {
	return func(generator *generateOptions) {
		generator.rpc = options
	}
}

// Generate emits Go files from a complete Tego plan via the protogen.Plugin (i.e. not directly to
// the filesystem).
func Generate(plugin *protogen.Plugin, plan Plan, opts ...GenerateOption) error {
	if diagnostics := fatalDiagnostics(plan); len(diagnostics) > 0 {
		return errors.New("plan contains fatal diagnostics")
	}
	options := newGenerateOptions(opts...)
	if err := validateRPCOptions(options.rpc); err != nil {
		return err
	}

	for _, file := range plan.Files {
		if file.IsEmpty() {
			continue
		}

		importPath := protogen.GoImportPath(file.Package.ImportPath)
		imports := newGeneratedImportGraph(file.Package.ImportPath, plan.PackageNames)

		// The discovery pass records exactly which imports this generation configuration uses. It
		// also gives us the complete set of package-level declarations before aliases are chosen.
		discoveryContent, err := renderGeneratedFilePass(plugin, file, importPath, imports, options)
		if err != nil {
			return fmt.Errorf("generate %s import discovery: %w", file.ProtoPath, err)
		}
		if err := imports.resolveDiscovery(discoveryContent, file.Package.Name); err != nil {
			return fmt.Errorf("generate %s imports: %w", file.ProtoPath, err)
		}

		// Rendering again lets every reference use the resolved alias from the outset. This avoids
		// fragile textual substitutions and verifies that both passes discover the same imports.
		finalContent, err := renderGeneratedFilePass(plugin, file, importPath, imports, options)
		if err != nil {
			return fmt.Errorf("generate %s final render: %w", file.ProtoPath, err)
		}
		content, err := imports.finalize(finalContent)
		if err != nil {
			return fmt.Errorf("generate %s imports: %w", file.ProtoPath, err)
		}

		// Both render passes were transient; only register the completed source with the plugin.
		output := plugin.NewGeneratedFile(file.Output.GeneratorPath, importPath)
		if _, err := output.Write(content); err != nil {
			return fmt.Errorf("generate %s content: %w", file.ProtoPath, err)
		}
	}

	return nil
}

// renderGeneratedFilePass renders one transient pass and returns its formatted content. The pass
// is skipped so only the finalized file is included in the plugin response.
func renderGeneratedFilePass(
	plugin *protogen.Plugin,
	file FilePlan,
	importPath protogen.GoImportPath,
	imports *generatedImportGraph,
	options generateOptions,
) ([]byte, error) {
	output := plugin.NewGeneratedFile(file.Output.GeneratorPath, importPath)
	defer output.Skip()
	generated := newGeneratedFile(output, imports)

	if err := generateFile(generated, file, options); err != nil {
		return nil, err
	}
	content, err := output.Content()
	if err != nil {
		return nil, fmt.Errorf("format generated content: %w", err)
	}
	return content, nil
}

func newGenerateOptions(opts ...GenerateOption) generateOptions {
	options := generateOptions{rpc: defaultRPCOptions()}
	for _, opt := range opts {
		opt(&options)
	}
	return options
}

func fatalDiagnostics(plan Plan) []Diagnostic {
	var diagnostics []Diagnostic
	for _, file := range plan.Files {
		for _, diagnostic := range file.Diagnostics {
			if diagnostic.Level == DiagnosticLevelFatal {
				diagnostics = append(diagnostics, diagnostic)
			}
		}
	}
	return diagnostics
}

func formatDiagnostics(diagnostics []Diagnostic) string {
	var out strings.Builder
	for i, diagnostic := range diagnostics {
		if i > 0 {
			out.WriteByte('\n')
		}
		out.WriteString("- ")
		out.WriteString(diagnostic.String())
	}
	return out.String()
}

func generateFile(g *generatedFile, file FilePlan, options generateOptions) error {
	g.P(generatedHeader)
	g.P("package ", file.Package.Name)
	g.P()

	for _, enum := range file.Enums {
		generateEnum(g, enum)
	}

	for _, oneof := range file.Oneofs {
		if err := generateOneof(g, oneof); err != nil {
			return err
		}
	}

	for _, structure := range file.Structs {
		if err := generateStruct(g, structure); err != nil {
			return err
		}
	}

	for _, helper := range file.RequestInlineHelpers {
		if err := generateServiceRequestInlineHelper(g, helper); err != nil {
			return err
		}
	}

	for _, helper := range file.ResponseInlineHelpers {
		if err := generateServiceResponseInlineHelper(g, helper); err != nil {
			return err
		}
	}

	if options.rpc.Enabled() {
		for _, service := range file.Services {
			if err := generateService(g, service, options.rpc); err != nil {
				return err
			}
		}
	}

	for _, mapping := range file.Mappings {
		if err := generateMapping(g, mapping); err != nil {
			return err
		}
	}

	return nil
}

func generateEnum(g *generatedFile, enum EnumPlan) {
	generateComment(g, "", enum.Comment)
	g.P("type ", enum.Name, " ", enumUnderlyingName(enum.Underlying))
	g.P()

	if len(enum.Constants) == 0 {
		return
	}

	g.P("const (")
	for _, constant := range enum.Constants {
		generateComment(g, "\t", constant.Comment)
		g.P("\t", constant.Name, " ", enum.Name, " = ", enumConstantLiteral(enum.Underlying, constant.Value))
	}
	g.P(")")
	g.P()
}

func generateOneof(g *generatedFile, oneof OneofPlan) error {
	generateComment(g, "", oneof.Comment)
	g.P("type ", oneof.Name, " interface {")
	g.P("\t", oneof.MarkerMethod, "()")
	g.P("}")
	g.P()

	for _, variant := range oneof.Variants {
		if err := generateOneofVariant(g, oneof, variant); err != nil {
			return fmt.Errorf("oneof %s variant %s: %w", oneof.ProtoName, variant.ProtoName, err)
		}
	}

	return nil
}

func generateOneofVariant(g *generatedFile, oneof OneofPlan, variant OneofVariantPlan) error {
	fieldType, err := generateType(g, variant.Type)
	if err != nil {
		return fmt.Errorf("field type: %w", err)
	}

	generateComment(g, "", variant.Comment)
	g.P("type ", variant.Name, " struct {")
	g.P("\t", variant.FieldName, " ", fieldType)
	g.P("}")
	g.P()
	g.P("func (", variant.Name, ") ", oneof.MarkerMethod, "() {}")
	g.P()
	return nil
}

func enumUnderlyingName(underlying EnumUnderlyingType) string {
	switch underlying {
	case EnumUnderlyingTypeInt:
		return "int"
	case EnumUnderlyingTypeString:
		return "string"
	default:
		return "uint"
	}
}

func enumConstantLiteral(underlying EnumUnderlyingType, value EnumConstantValue) string {
	switch underlying {
	case EnumUnderlyingTypeInt:
		return strconv.Itoa(value.Int)
	case EnumUnderlyingTypeString:
		return strconv.Quote(value.String)
	default:
		return strconv.FormatUint(uint64(value.Uint), 10)
	}
}

func generateStruct(g *generatedFile, structure StructPlan) error {
	generateComment(g, "", structure.Comment)
	g.P("type ", structure.Name, " struct {")
	for _, field := range structure.Fields {
		fieldType, err := generateType(g, field.Type)
		if err != nil {
			return fmt.Errorf("field %s: %w", field.ProtoName, err)
		}

		generateComment(g, "\t", field.Comment)
		if tags := structTagLiteral(field.Tags); tags != "" {
			g.P("\t", field.Name, " ", fieldType, " ", tags)
		} else {
			g.P("\t", field.Name, " ", fieldType)
		}
	}
	g.P("}")
	g.P()
	return nil
}

func generateType(g *generatedFile, plan TypePlan) (string, error) {
	switch plan.Kind {
	case TypeKindScalar:
		return scalarTypeName(plan.Scalar), nil
	case TypeKindEnum, TypeKindStruct, TypeKindExternal, TypeKindCustom, TypeKindOneof:
		return generateNamedType(g, plan.Ref), nil
	case TypeKindPointer:
		elem, err := generateElemType(g, plan.Elem, "pointer")
		return "*" + elem, err
	case TypeKindSlice:
		elem, err := generateElemType(g, plan.Elem, "slice")
		return "[]" + elem, err
	case TypeKindMap:
		key, err := generateElemType(g, plan.Key, "map key")
		if err != nil {
			return "", err
		}
		value, err := generateElemType(g, plan.Value, "map value")
		return "map[" + key + "]" + value, err
	case TypeKindOmittable:
		elem, err := generateElemType(g, plan.Elem, "omittable")
		if err != nil {
			return "", err
		}
		ref := GoTypeRef{ImportPath: omittableImportPath, Name: "Value"}
		return generateNamedType(g, ref) + "[" + elem + "]", nil
	case TypeKindEmptyStruct:
		return "struct{}", nil
	default:
		return "", fmt.Errorf("unsupported type kind %d", plan.Kind)
	}
}

func generateElemType(g *generatedFile, plan *TypePlan, context string) (string, error) {
	if plan == nil {
		return "", fmt.Errorf("%s type is missing an element", context)
	}
	return generateType(g, *plan)
}

func scalarTypeName(kind ScalarKind) string {
	switch kind {
	case ScalarKindBool:
		return "bool"
	case ScalarKindInt32:
		return "int32"
	case ScalarKindInt64:
		return "int"
	case ScalarKindUint32:
		return "uint32"
	case ScalarKindUint64:
		return "uint"
	case ScalarKindFixedInt64:
		return "int64"
	case ScalarKindFixedUint64:
		return "uint64"
	case ScalarKindFloat32:
		return "float32"
	case ScalarKindFloat64:
		return "float64"
	case ScalarKindBytes:
		return "[]byte"
	case ScalarKindAny:
		return "any"
	default:
		return "string"
	}
}

func generateNamedType(g *generatedFile, ref GoTypeRef) string {
	switch {
	case ref.Pointer != nil:
		return "*" + generateNamedType(g, *ref.Pointer)
	case ref.Slice != nil:
		return "[]" + generateNamedType(g, *ref.Slice)
	case ref.Array != nil:
		return "[" + strconv.FormatInt(ref.ArrayLen, 10) + "]" + generateNamedType(g, *ref.Array)
	case ref.MapKey != nil && ref.MapValue != nil:
		return "map[" + generateNamedType(g, *ref.MapKey) + "]" + generateNamedType(g, *ref.MapValue)
	}

	var name string
	if ref.ImportPath == "" {
		name = ref.Name
	} else {
		name = g.qualify(ref.ImportPath, ref.Name)
	}
	if len(ref.Args) == 0 {
		return name
	}

	args := make([]string, 0, len(ref.Args))
	for _, arg := range ref.Args {
		args = append(args, generateNamedType(g, arg))
	}
	return name + "[" + strings.Join(args, ", ") + "]"
}

func structTagLiteral(tags []StructTagPlan) string {
	if len(tags) == 0 {
		return ""
	}

	parts := make([]string, 0, len(tags))
	for _, tag := range tags {
		parts = append(parts, tag.Key+":"+strconv.Quote(tag.Value))
	}
	return "`" + strings.Join(parts, " ") + "`"
}

func generateComment(g *generatedFile, indent string, comment string) {
	comment = strings.TrimSpace(comment)
	if comment == "" {
		return
	}

	prefix := indent + "// "
	for _, line := range strings.Split(comment, "\n") {
		for _, wrapped := range wrapCommentLine(prefix, strings.TrimSpace(line)) {
			g.P(wrapped)
		}
	}
}

func wrapCommentLine(prefix, text string) []string {
	if text == "" {
		return []string{strings.TrimSuffix(prefix, " ")}
	}

	width := maxGeneratedLineWidth - len(prefix)
	if width < 1 {
		width = 1
	}

	parts := wrapWords(text, width)
	lines := make([]string, 0, len(parts))
	for _, part := range parts {
		lines = append(lines, prefix+part)
	}
	return lines
}

func wrapWords(text string, width int) []string {
	var lines []string
	var line string

	for _, word := range strings.Fields(text) {
		for len(word) > width {
			prefix := word[:width]
			word = word[width:]
			if line != "" {
				lines = append(lines, line)
				line = ""
			}
			lines = append(lines, prefix)
		}

		if line == "" {
			line = word
			continue
		}
		if len(line)+1+len(word) <= width {
			line += " " + word
			continue
		}

		lines = append(lines, line)
		line = word
	}

	if line != "" {
		lines = append(lines, line)
	}
	return lines
}
